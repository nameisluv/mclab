     AREA FIRSTTENEVENMEM,CODE
	 MOV R0,#2	    	;EVEN NUMBERS
	 MOV R1,#0	    	;COUNTER
	 MOV R2,#0	    	;SUM
	 MOV R3,#0x40000000 ;LOAD FIRST ADDRESS IN REGISTER
NEXT ADD R2,R2,R0   	;ADD EVEN NUMBER TO SUM
	 ADD R0,R0,#2   	;CALCULATE NEXT EVEN NUMBER
	 ADD R1,R1,#1   	;INCREEMENT COUNTER
	 CMP R1,#10     	;TERMINATING CONDITION
	 BNE NEXT			;BRANCH IF NOT EQUAL
	 STR R2,[R3]		;USE POSTINDEXING TO STORE VALUE
GO   B GO
	 END